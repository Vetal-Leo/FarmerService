            identy.Body = "<h2>Сервис Farmer.</h2>Благодарим Вас за выбор сервиса \"Farmer\".<br/>" +
                          "<h3>Ваш Логин : " + email + "<br/>Ваш пароль : " + password +
                          "</h3>Для завершения регистрации и активации Вашего аккаунта перейдите по ссылке: " +
                          " <a href=\"" + callbackUrl + "\">Активировать аккаунт</a>";
            await emailservice.SendAsync(identy);
        }

