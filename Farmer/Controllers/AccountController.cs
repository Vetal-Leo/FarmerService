    public class EmailService
    {
        public Task SendAsync(IdentityMessage message)
        {
            // настройка логина, пароля отправителя
            const string @from = "farmer-origin@mail.ru";
            const string pass = "87pobeda78";

            // адрес и порт smtp-сервера, с которого отправляем письмо
            var client = new SmtpClient("smtp.mail.ru", 587)
            {
                DeliveryMethod = SmtpDeliveryMethod.Network,
                UseDefaultCredentials = false,
                Credentials = new NetworkCredential(@from, pass),
                EnableSsl = true
            };


            // письмо: message.Destination - адрес получателя
            var mail = new MailMessage(from, message.Destination)
            {
                Subject = message.Subject,
                Body = message.Body,
                Priority = MailPriority.High,
                IsBodyHtml = true
            };

            return client.SendMailAsync(mail);
        }
    }

    public class FarmerAuthorize : AuthorizeAttribute
    {
        public override void OnAuthorization(AuthorizationContext filterContext)
        {
            // If the user is not authorized, he is redirected to AccountSimple/Login
            var auth = filterContext.HttpContext.User.Identity.IsAuthenticated;
            if (!auth)
            {
                filterContext.Result = new RedirectToRouteResult(
                    new RouteValueDictionary {
                { "controller", "AccountSimple" }, { "action", "Login" }
                });
            }
        }
